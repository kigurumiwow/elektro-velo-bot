def generate_contract_pdf(data: dict) -> io.BytesIO:
    buf = io.BytesIO()
    doc = SimpleDocTemplate(
        buf, pagesize=A4,
        leftMargin=18 * mm, rightMargin=18 * mm, topMargin=16 * mm, bottomMargin=16 * mm
    )
    flow = build_contract_flowables(data)
    doc.build(flow)
    buf.seek(0)
    return buf
